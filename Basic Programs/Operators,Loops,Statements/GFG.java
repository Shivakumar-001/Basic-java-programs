//java Program to implement Logical Operators
import java.io.*;
class GFG
{
    public static void main(String args[])
    {
        boolean x = true;
        boolean y= false;
        System.out.println("x&&y:"+(x&&y));
        System.out.println("x||y:"+(x||y));
        System.out.println("!x:"+(!x));
    }
}

/* OutPut
 
x&&y:false
x||y:true
!x:false

 */