//Java Program to implement Arithmetic Operators
import java.io.*;
class Arithmetic {

    public static void main(String args[]) {
        int a = 10, b = 3;
        System.out.println("a + b =" + (a + b));
        System.out.println("a - b =" + (a - b));
        System.out.println("a * b =" + (a * b));
        System.out.println("a / b =" + (a / b));
        System.out.println("a % b =" + (a % b));
    }
}
/* OUTPUT 

a + b =13
a - b =7
a * b =30
a / b =3
a % b =1

*/